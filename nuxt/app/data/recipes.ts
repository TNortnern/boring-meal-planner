export interface RecipeIngredient {
  name: string
  amount: string
  macros?: { protein: number, carbs: number, fat: number }
}

export interface Recipe {
  id: number
  name: string
  description: string
  prepTime: number
  cookTime?: number
  boringScore: number
  difficulty: 'easy' | 'medium' | 'hard'
  goalTags: ('cut' | 'maintain' | 'gain')[]
  proteinSource: string
  tags: string[]
  source: 'homemade' | 'restaurant' | 'fast_food'
  macros: {
    calories: number
    protein: number
    carbs: number
    fat: number
  }
  nutrition?: {
    fiber?: number // grams
    sugar?: number // grams
    sodium?: number // mg
    cholesterol?: number // mg
    saturatedFat?: number // grams
  }
  ingredients: number
  ingredientsList?: RecipeIngredient[]
  instructions?: string[]
  tips?: string
}

// Helper to get difficulty based on prep time and ingredient count
export const getDifficulty = (prepTime: number, ingredientCount: number): 'easy' | 'medium' | 'hard' => {
  if (prepTime <= 10 && ingredientCount <= 4) return 'easy'
  if (prepTime <= 20 && ingredientCount <= 6) return 'medium'
  return 'hard'
}

