export const getRecipesByProtein = (protein: string) => {
  return boringRecipes.filter(r => r.proteinSource === protein)
}

export const getRecipesByGoal = (goal: 'cut' | 'maintain' | 'gain') => {
  return boringRecipes.filter(r => r.goalTags.includes(goal))
}

export const getRecipesByTag = (tag: string) => {
  return boringRecipes.filter(r => r.tags.includes(tag))
}

export const getQuickRecipes = (maxPrepTime: number = 10) => {
  return boringRecipes.filter(r => r.prepTime <= maxPrepTime)
}

export const getMostBoringRecipes = (minScore: number = 9) => {
  return boringRecipes.filter(r => r.boringScore >= minScore)
}
